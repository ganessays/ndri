           2.&nbsp;&nbsp; &nbsp;On-site sanitation systems- faecal sludge management<br />
           3.&nbsp;&nbsp; &nbsp;Waste water management- Grey water &amp; black water<br />
           4. &nbsp; &nbsp;Storm water management (focusing on localized infiltration and integrating with greenery)<br />
           5.&nbsp;&nbsp; &nbsp;Solid waste Management<br />
           6.&nbsp;&nbsp; &nbsp;Greenery- public parks, greenery belts<br />
           7.&nbsp;&nbsp; &nbsp;Special category solid waste and waste water</p>
